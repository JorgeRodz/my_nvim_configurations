local set = vim.opt

set.autoindent = true    --"New lines inherit the indentation of previous lines.
set.clipboard = unnamed    --"To enable the usage of the systema clipboard"
set.number = true        --"To set number lines on left side"
set.mouse = "a"              --"To use the mouse in neovim
set.relativenumber = true  --"To set left numbers relative to the line we are in"
set.smarttab = true      --"Convert tabs to spaces.
set.shiftwidth = 2         --"To make 2 spaces as Tab
set.softtabstop = 2        --"To make 2 spaces as Tab
set.tabstop = 2		   --"To make 2 spaces as Tab
set.expandtab = true       --"To replace tabs with whitespaces"
set.fileencoding = 'utf-8'
set.syntax = 'on'         -- In order to apply syntax on text and icons
set.splitright = true   -- To split new buffers on the right side of the window
set.cursorline = true
